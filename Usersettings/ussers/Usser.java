public class Usser {
    private int ID;
    private int ussersId;
    private int date;
    private String name;

    public int getID() {
        return ID;
    }

    public void setID(int ID) {
        this.ID = ID;
    }

    public int getUssersId() {
        return ussersId;
    }

    public void setUssersId(int ussersId) {
        this.ussersId = ussersId;
    }

    public int getDate() {
        return date;
    }

    public void setDate(int date) {
        this.date = date;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
